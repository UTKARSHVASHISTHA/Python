#Assignment Operator is "=".

#Arithmetic Operators:+,-,/,*,**,//

#Arithmetic Assignment:+=,-=,/=,*=,**=,//=


num1=int(input("Enter a Number: "))

#Assignment Operator

num2=num1
print("Value of num2 is :",num2)

#Arithmetic Assignment

num2+=10  #num2=num2+10
print("Value of num2 is: ",num2)

num2-=5   #num2=num2-5
print("Value of num2 is: ",num2)

num2*=8   #num2=num2*8
print("Value of num2 is: ",num2)

num2/=2   #num2=num2%2
print("Value of num2 is: ",num2)

num2%=2   #num2=num2%2
print("Value of num2 is: ",num2)

num2**5   #num2=num2**5
print("Value of num2 is: ",num2)

num2//=2  #num2=num2/2
print("Value of num2 is: ",num2)
